#include "iperson.h"

// --------------------------------------------------------------------------
// constructor 
// --------------------------------------------------------------------------
IPerson::IPerson()
{
}

// --------------------------------------------------------------------------
// constructor with parameters
// --------------------------------------------------------------------------
IPerson::IPerson(string firstName, string lastName, int hashKey)
{
	m_firstName = firstName;
	m_lastName = lastName;
	m_hashKey = hashKey;

}

// --------------------------------------------------------------------------
// destructor
// --------------------------------------------------------------------------
IPerson::~IPerson()
{
}

// --------------------------------------------------------------------------
// accessor for m_hashKey
// --------------------------------------------------------------------------
int IPerson::getHashKey()
{
	return m_hashKey;
}

// --------------------------------------------------------------------------
// accessor for m_firstName
// --------------------------------------------------------------------------
string IPerson::getFirstName()
{
	return m_firstName;
}

// --------------------------------------------------------------------------
// accessor for m_lastName
// --------------------------------------------------------------------------
string IPerson::getLastName()
{
	return m_lastName;
}
